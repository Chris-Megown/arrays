
public class FriendNames {

	public static void main(String[] args) {
		String[] friendNames = new String[3];
		friendNames[0] = "Jake";
		friendNames[1] = "Jeff";
		friendNames[2] = "Luke";

		for (int idx = 0; idx < friendNames.length; idx++) {
			System.out.println(friendNames[idx]);
		}

	}

}
